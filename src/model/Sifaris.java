
package model;


public class Sifaris extends AbstractModel{
    private String resName;
    private int say;
    private Float price;
    private MenuIn menuIn;
    private String sifaris;

    public String getResName() {
        return resName;
    }

    public void setResName(String resName) {
        this.resName = resName;
    }

    public int getSay() {
        return say;
    }

    public void setSay(int say) {
        this.say = say;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }

    public MenuIn getMenuIn() {
        return menuIn;
    }

    public void setMenuIn(MenuIn menuIn) {
        this.menuIn = menuIn;
    }

    public String getSifaris() {
        return sifaris;
    }

    public void setSifaris(String sifaris) {
        this.sifaris = sifaris;
    }
    
    
}
